#dict methods:

# 1.clear():remove all the elements from the dictonary.
'''
dict = {'a':1,'b':2,'c':3}
dict.clear()
print(dict)


#output:
{}
'''

#--------------------------------------------------------------------------------------------------------------------------------------

# 2.copy():return a copy of dictionary.
'''
dic = {'a':1,'b':2,'c':3,'d':4}

dic1=dic.copy()

print(dic1)


#output:
{'a': 1, 'b': 2, 'c': 3, 'd': 4}
'''

#------------------------------------------------------------------------------------------------------------------------------------------------------------

# 3.fromkeys():returns a dictonary with the specified keys and value.
'''
x = ('key1','key2','key3')

y = 1

dict2 = dict.fromkeys(x,y)

print(dict2)


#output:
{'key1': 1, 'key2': 1, 'key3': 1}
'''

#----------------------------------------------------------------------------------------------------------------------------------------------------------

# 4.get():returs the value of the specified key.

'''
dic = {'a':1,'b':2,'c':3}

dic1 = dic.get('b')

print(dic1)



#output:
2
'''

#-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


# 5.items():Returns a list containing a tuple for each key value pair
'''
dic = {1:'a',2:'b',3:'c'}

x = dic.items()

print(x)


#output:
dict_items([(1, 'a'), (2, 'b'), (3, 'c')])
'''

#-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

# 6.keys(),values():Returns a list containing the dictionary's keys
'''
dic = {'a':'b','c':'d','e':'f','g':'h'}

x = dic.values()

y = dic.keys()
print(x)
print(y)

#output:
dict_values(['b', 'd', 'f', 'h'])
dict_keys(['a', 'c', 'e', 'g'])
'''

#-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

# 7.pop():Removes the element with the specified key
'''
dic = {'a':10,'b':20,'c':30,'d':40}

dic.pop('a')
print(dic)
dic.pop('b')
print(dic)

#output:
{'b': 20, 'c': 30, 'd': 40}
{'c': 30, 'd': 40}
'''

#--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

# 8.popitem():Removes the last inserted key-value pair
'''
dic = {'a':'tagoor','b':10,'c':'d'}

dic.popitem()

print(dic)

#output:
{'a': 'tagoor', 'b': 10}
'''

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

# 9.update():Updates the dictionary with the specified key-value pairs
'''
dic = {'a':1,'b':2,'c':3,'d':4}

dic.update({'e':5,'f':6})

print(dic)


#output:
{'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5, 'f': 6}
'''























